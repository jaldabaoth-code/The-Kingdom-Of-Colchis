            <p>
                Colchís, Kolkhís or Qulḫa which existed from the c. 13th to the 1st
                centuries BC, is regarded as an early ethnically Georgian polity; the
                name of the Colchians was used as the collective term for early
                Kartvelian tribes which populated the eastern coast of the Black Sea
                in Greco-Roman ethnography. The name Colchis is thought to have
                derived from the Urartian Qulḫa. In the late eighth century BC,
                Sarduri II the King of Urartu, inscribed his victory over Qulḫa on a
                stele; however, the exact location of Qulḫa is disputed. Some scholars
                argue the name Qulḫa (Colchís) originally referred to a land to the
                west of Georgia. According to the scholar of Caucasian studies Cyril
                Toumanoff: Colchis appears as the first Caucasian State to have
                achieved the coalescence of the newcomer. Colchis can be justly
                regarded as not a proto-Georgian, but a Georgian (West Georgian)
                kingdom. . . .It would seem natural to seek the beginnings of Georgian
                social history in Colchis, the earliest Georgian formation. A second
                South Caucasian tribal union emerged in the thirteenth century BC on
                the Black Sea coast. According to most classic authors, a district
                which was bounded on the south-west by Pontus, on the west by the
                Black Sea as far as the river Corax (probably the present day Bzyb
                River, Abkhazia, Georgia), on the north by the chain of the Greater
                Caucasus, which lay between it and Asiatic Sarmatia, on the east by
                the Kingdom of Iberia and Montes Moschici (now the Lesser Caucasus),
                and on the south by Armenia. The westward extent of the country is
                considered differently by different authors: Strabo makes Colchis
                begin at Trabzon, while Ptolemy, on the other hand, extends Pontus to
                the Rioni River. The Greek name Kolchís (Κολχίς) is first used to
                describe a geographic area in the writings of Aeschylus and Pindar.
                Earlier writers speak of the "Kolchian" (Κολχίδα) people and their
                mythical king Aeëtes (Αἰήτης), as well as his eponymous city Aea or
                Aia (Αἶα), but don't make explicit references to a Kolchis nation or
                region. The main river was known as the Phasis (now Rioni) and was,
                according to some writers the southern boundary of Colchis, but more
                probably flowed through the middle of that country from the Caucasus
                west into the Euxine, and the Anticites or Atticitus (now Kuban).
                Arrian mentions many others by name, but they would seem to have been
                little more than mountain torrents: the most important of them were
                Charieis, Chobus or Cobus, Singames, Tarsuras, Hippus, Astelephus,
                Chrysorrhoas, several of which are also noticed by Ptolemy and Pliny.
                The chief towns were Dioscurias or Dioscuris (under the Romans called
                Sebastopolis, now Sukhumi) on the seaboard of the Euxine, Sarapana
                (now Shorapani), Phasis (now Poti), Pityus (now Pitsunda), Apsaros
                (now Gonio), Surium (now Vani), Archaeopolis (now Nokalakevi),
                Macheiresis, and Cyta or Cutatisium or Aia (now Kutaisi), the
                traditional birthplace of Medea. Scylax mentions also Mala or Male,
                which he, in contradiction to other writers, makes the birthplace of
                Medea.
